
import java.awt.*;
import javax.swing.*;
import java.util.*;

public class APerson
{
  private String fname;
  private String ssn;
  private int age;
  
  public void setFName(String f)
  {
    fname=f;
	}
	
	public void setSSN(String f)
  {
    ssn=f;
	}
	public void setAge(int f)
  {
    age=f;
	}
	public String getFName()
	{
	  return fname;
	}
	public String getSSN()
	{
	  return ssn;
	}
	public int getAge()
	{
	  return age;
	}
	
}	
	
	
	
	
	
	
	
